-----------------------------------------------------------------------------------------------------------------------

module Gpx
(
   Gpx(..),
   Route(..)
)
where

-----------------------------------------------------------------------------------------------------------------------

import Data.Text(Text)

-----------------------------------------------------------------------------------------------------------------------

data Gpx = Gpx
   {
      gpxRoute :: Route
   }
   deriving(Show)


data Route = Route
   {
      rteName :: Maybe Text,
      rtePoints :: [RoutePoint]
   }
   deriving(Show)


data RoutePoint = RoutePoint
   {
      rteptLat :: Double,
      rteptLon :: Double
   }
   deriving(Show)

